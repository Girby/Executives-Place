     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = User::find($id);
        $currency = new Currency;

        $currency = $currency->convert($user->rate, $user->currency);
        return view('users-get')->with(compact('user', 'currency'));
    }

